public class Function5 {
    public static void main(String args[]){
        // swap - values exchange
        int a = 5;
        int b = 10;

        // SWAP
        int temp= a;// TEMP = 5 
        a = b;// ABHI B = 10 HAI IMPILES A = 10
        b = temp;// THEN B = 5 
        /*ISKE BAD B KI VALUE 5 HOGI OR A KI 10 */
        System.out.println("a =" +a);
        System.out.println("b =" +b);
    }
    
}
